package Uge35og36;

public class Multiplikationstabel { /// Eksempler på for-løkke ///
    public static void main(String[] args) {
        for (int i=1; i<=10; i++) {
            for (int j=1; j<=10; j++){
                System.out.print(i*j+"\t");  /// option+shift+7 = \t = lave en tabel i println ///
        }
            System.out.println();
        }
    }
}
